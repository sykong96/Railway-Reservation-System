	<h1> Types of Train Services</h1>
	<span class="border"></span>
	<p>Today, KTM has become one of the most important transport mode use by traveler in Malaysia. There are over 100,000 commuters who use Commuter Train service provided by KTM and another 13,000 customers who use ETS (Electric Train Service) daily.

KTM is providing 3 types of train services, KTM Intercity train service, KTM Komuter train service and KTM Electric Train Service (ETS).

KTM Intercity has introduced over a decade ago. These trains are operating within Peninsular Malaysia, Singapore and Thailand. Most of the KTM Intercity depart from Kuala Lumpur Sentral Station. There are cabins and coaches with beds which allow customers to take a nap within the journey.

KTM Komuter was introduced in 1995. It operates within Kuala Lumpur and the surrounding sub-urban areas. It is a very popular mode of transport to commute to Kuala Lumpur. There are cabins specially designated for female passenger’s usage.

ETS is the latest train service that introduced by KTM. It is the fastest meter gauge train service in Malaysia. It operates along the electrified and double-tracked rail track of the West Coast Line between Gemas and Padang Besar on the Malaysia-Thai border. The train can travel with speed up to 140 kilometers per hour.</p>
