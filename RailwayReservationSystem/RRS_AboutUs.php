  <p>KTM (Keretapi Tanah Melayu) was previously known as the Federated Malay States Railways (FMSR) and the Malayan Railways Administration (MRA). In 1962, KTM rebranded to its current name. The company was corporatized in 1992, but remains wholly owned by the Malaysian government.

KTM mission is to be the choice mode of transportation for travelers by providing a safe, efficient and reliable integrated rail services.

KTM operates under 3 brandings KTM Intercity for a group of diesel-hauled intercity train services which connects Peninsular Malaysia, Singapore and Thailand. KTM ETS which stands for Electric Train Service which operates the West Coast Line between Germas and Padang Besar on the Malaysia-Thai border. Lastly KTM Komuter provides local rail services within Kuala Lumpur and Klang Valley.</p>
